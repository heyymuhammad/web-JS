function add(){
    a1 = parseInt(prompt("Enter the first number to add."));
    a2 = parseInt(prompt("Enter the second number to add."));
    document.write(a1+a2);
}

function product(){
    m1 = parseInt(prompt("Enter the first number to multiply."));
    m2 = parseInt(prompt("Enter the second number to multiply."));
    document.write(m1*m2);
}

